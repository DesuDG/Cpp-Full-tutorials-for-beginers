#include <iostream>
#include <string>
using namespace std;
 
int main () {
  // Creating variables
  int a = 10;               // Integer 
  float b = 10.23;     // Floating point number
  double c = 20.98;   // Floating point number
  char d = 'T';         // Character
  bool e = true;       // Boolean
  string f = "Hello";   // String
   
  // Print variable values
  cout << "int: " << a << "\n";
  cout << "float: " << b << "\n";
  cout << "double: " << c << "\n";
  cout << "char: " << d << "\n";
  cout << "bool: " << e << "\n";
  cout << "string: " << f << "\n";
 
  return 0;
}