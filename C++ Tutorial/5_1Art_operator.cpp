#include <iostream>
using namespace std;

int main() {
    // Arthematic Operator
  int x = 7;
  int y = 4;
  cout <<"sum=" << x + y << endl;
  cout <<"difference=" << x - y << endl;
  cout <<"product=" << x * y << endl;
  cout <<"remainder=" << x % y << endl;
  cout <<"increment=" << ++ x << endl;
  cout <<"decrement=" << -- y  ;

    
  return 0;
}